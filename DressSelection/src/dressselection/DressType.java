    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Dress d;
        if (this.jComboBox1.getSelectedItem().toString().equals("Shirt")) {
            d = new Shirt();
            ((Shirt) d).setSize(this.jComboBox2.getSelectedItem().toString());

        } else {
            d = new Trouser();
            ((Trouser) d).setLength(this.jComboBox2.getSelectedItem().toString());
        }
        d.setPrice(Double.parseDouble(this.jTextField1.getText()));
        if (p.addItem(d)) {
            this.jTextField1.setText("");
            JOptionPane.showMessageDialog(this, "Added", "Information",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "exceeding the allowable items,won't add here on,Press Show to get the summary", "Information",
                    JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

