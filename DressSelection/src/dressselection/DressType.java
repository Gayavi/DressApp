    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        if (this.jComboBox1.getSelectedItem().toString().equals("Shirt")) {
            this.jComboBox2.removeAllItems();
            this.jComboBox2.addItem("small");
            this.jComboBox2.addItem("medium");
            this.jComboBox2.addItem("large");
            this.jComboBox2.addItem("excel");
        } else {
            this.jComboBox2.removeAllItems();                                              
          
            this.jComboBox2.addItem("short");
            this.jComboBox2.addItem("long");
            this.jComboBox2.addItem("three quarter");

        }
    }//GEN-LAST:event_jComboBox1ItemStateChanged

